//Нет обработки пустого массива: Если массив пустой, произойдет деление на ноль.
function getAverage(arr) {
    if (arr.length === 0) {
        return 0; // или можно вернуть null, NaN, или другое значение для пустого массива
    }

    let sum = 0;
    for (let elem of arr) {
        sum += elem;
    }
    
    return sum / arr.length;
}             
